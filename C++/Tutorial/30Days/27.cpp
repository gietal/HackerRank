#include <iostream>
using namespace std;

int main() {
    cout << "5" << endl;

	// cancelled
	cout << "4 3" << endl;
	cout << "-1 -3 4 2" << endl;

	// not cancelled
	cout << "5 2" << endl;
	cout << "0 -1 2 1 4" << endl;

	// cancelled
	cout << "3 3" << endl;
	cout << "-1 -1 1" << endl;

	// not cancelled

	cout << "3 3" << endl;
	cout << "0 1 2" << endl;

	// cancelled

	cout << "3 3" << endl;
	cout << "1 -1 -1" << endl;
}
